export const getFriendById = (Id) => {
    return friendsList.find((friend) => {
        return friend.Id === Id;
    })
}

export const addMessage = (friendId, message) => {
    let friend = getFriendById(friendId);
    let newMessage = {
        Type: "Sent",
        Message: message,
        Time: new Date(),
    };
    friend.Messages.push(newMessage);
}